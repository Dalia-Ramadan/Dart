void main(){
  List grade = [50,60,70,90,55,77,88,95,100];
  print(grade.map((e)=>{
    e>=85 && e<=100 ? 'A' :
    e>=75 && e<85 ? 'B' :
    e>=65 && e<75 ? 'C' :
    e>=50 && e<65 ? 'D' :
    e>=0 && e<50 ? 'F' : 'something wrong'
  }));
}
// Q:
// تحويل الدرجات إلى تقديرات باستخدام map
// 🧠 لدينا مصفوفة من الدرجات مثل: [90, 75, 60, 40]
// المطلوب: استخدام دالة map لتحويل كل درجة إلى التقدير المناسب:

// 85 فأكثر → ممتاز
// 70 فأكثر → جيد
// 50 فأكثر → مقبول
// أقل من 50 → ضعيف